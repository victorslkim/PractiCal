import Foundation

struct CalendarConstants {
    // Time column width for all calendar views
    static let timeColumnWidth: CGFloat = 40
    
    // Standard hour height for time grids
    static let hourHeight: CGFloat = 72
    
    // Number of days in a week
    static let daysInWeek: CGFloat = 7
    
    // Horizontal padding for layouts
    static let horizontalPadding: CGFloat = 8
    
    // Time label trailing padding
    static let timeLabelTrailingPadding: CGFloat = 8
    
    // Minimum drag distance to trigger swipe navigation
    static let swipeThreshold: CGFloat = 0.00001
}